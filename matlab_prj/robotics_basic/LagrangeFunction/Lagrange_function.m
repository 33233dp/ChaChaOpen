% 《机器人学 第四版 清华大学出版社》p92，4.1.1 刚体的动能与位能
% 题配图在当前目录的 [图4.1.png]。
%
% 本脚本使用 MATLAB Symbolic Math Toolbox 做拉格朗日方程推导。
% 主要步骤：
%   1. 用 syms 定义时间、广义坐标、系统参数等符号量；
%   2. 根据位移函数 x0(t)、x1(t) 求速度；
%   3. 写出动能 K、势能 P、耗散函数 D、外力虚功 W；
%   4. 构造拉格朗日函数 L = K - P；
%   5. 对物体 0 使用拉格朗日方程推导运动方程；
%   6. 用 solve 从方程中解出外力 F 的表达式。

%% 定义符号变量和符号函数

% syms 用于创建符号变量或符号函数，常用于公式推导。
% 语法示例：
%   syms a b           创建符号变量 a、b；
%   syms x(t)          创建关于 t 的符号函数 x(t)；
%   syms m real        创建实数符号变量 m。
%
% 这里 t 表示时间。末尾的分号 ; 用于禁止 MATLAB 在命令行自动显示结果。
syms t;

% K 表示动能。这里预先声明为符号变量。
% 注意：后面会把 K 重新赋值为具体的动能表达式。
syms K;

% x0(t)、x1(t) 是关于时间 t 的符号函数，表示两个物体的位置。
% 写成 x0(t) 后，MATLAB 会知道 x0 是函数而不是普通代数变量；
% 因此 diff(x0,t) 会得到 dx0/dt，也就是速度。
syms x0(t) x1(t);

% M0、M1 表示两个物体的质量。
syms M0 M1;

% k 表示弹簧刚度，g 表示重力加速度，c 表示阻尼系数。
syms k;
syms g;
syms c;

% F 表示外力。后面会通过 solve 求出 F 的表达式。
syms F;

%% 求广义速度

% diff 用于求导。
% 语法示例：
%   diff(f,t)      对表达式或函数 f 关于 t 求一阶导数；
%   diff(f,t,2)    对 f 关于 t 求二阶导数。
%
% dx0、dx1 分别表示 x0(t)、x1(t) 对时间的一阶导数，即速度。
dx0 = diff(x0,t);
dx1 = diff(x1,t);

%% 写出系统能量、耗散和外力虚功

% 动能 K = 1/2*M0*dx0^2 + 1/2*M1*dx1^2。
% 两个物体都只做平动，所以分别使用 1/2*m*v^2。
K = 1/2 * M0 * dx0^2 + 1/2 * M1 * dx1^2;

% 势能 P：
%   1/2*k*(x1 - x0)^2 是弹簧弹性势能；
%   -M1*g*x1、-M0*g*x0 是重力势能项。
% 这里符号正负取决于坐标正方向和教材建模约定。
P = 1/2 * k * (x1 - x0)^2 - M1 * g * x1 - M0 * g * x0;

% D 是瑞利耗散函数，常用于表示线性阻尼造成的能量耗散。
% 这里阻尼器两端相对速度为 dx1 - dx0，因此 D = 1/2*c*(dx1 - dx0)^2。
D = 1/2*c*(dx1 - dx0)^2;

% W 表示外力 F 对两个坐标所做的虚功相关表达式。
% F*x1 - F*x0 表示同一外力对 x1 和 x0 的作用方向相反。
W = F*x1 - F*x0;

%% 显示中间表达式

% disp 用于在命令行显示文本或变量值。
% 语法示例：
%   disp('K = ')    显示字符串 K =；
%   disp(K)         直接显示变量 K 的值。
%
% pretty 用于把符号表达式以更接近数学排版的形式显示在命令行。
% 它只负责“漂亮地显示”，不会改变变量本身的值。
% 语法示例：
%   pretty(K)       以二维数学形式显示符号表达式 K。
disp('K = ')
pretty(K)

disp('P = ')
pretty(P)

disp('D = ')
pretty(D)

disp('W = ')
pretty(W)

%% 构造拉格朗日函数

% 拉格朗日函数定义为：
%   L = K - P
% 其中 K 是动能，P 是势能。
L = K - P;

disp('L = ')
pretty(L)

%% 利用拉格朗日方程推导物体 0 的运动方程

% 对广义坐标 x0，保守系统的拉格朗日方程形式为：
%   d/dt(∂L/∂dx0) - ∂L/∂x0 = Q0
%
% 这里左侧记作 Fi0_Lagrange。
% diff(L,dx0) 表示对速度 dx0 求偏导；
% diff(diff(L,dx0),t) 表示再对时间 t 求全导数；
% diff(L,x0) 表示对广义坐标 x0 求偏导。
Fi0_Lagrange  = diff(diff(L,dx0), t) - diff(L, x0);

disp('Fi0_Lagrange  = ')
pretty(Fi0_Lagrange)

%% 建立方程并求解外力 F

% F0 表示作用在物体 0 上的广义力。
% -F 表示外力 F 对物体 0 的方向与 x0 正方向相反；
% c*(dx1 - dx0) 表示阻尼力项，与两个物体的相对速度有关。
F0 = -F + c * (dx1 - dx0);

% == 用于建立符号方程。
% eq0 不是逻辑 true/false，而是一个 symbolic equation，
% 表示“F0 等于 Fi0_Lagrange”这个待求解的方程。
eq0 = F0 == Fi0_Lagrange;

% solve 用于求解符号方程。
% 语法示例：
%   solve(eq, x)            从方程 eq 中解出变量 x；
%   solve([eq1,eq2],[x,y])  联立多个方程，解多个未知量。
%
% 这里 solve(eq0, F) 表示：把 F 当作未知量，从 eq0 中解出 F。
F_sol = solve(eq0, F);

% simplify 用于化简符号表达式，使结果更紧凑。
% 最后用 pretty 以数学排版形式显示化简后的 F。
disp('F = ');
pretty(simplify(F_sol));
